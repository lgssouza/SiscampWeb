package br.com.entidades;

public class Sobreaviso {

	private int idSobreaviso;
	private int funcMatricula;
	private String data;

	public int getIdSobreaviso() {
		return idSobreaviso;
	}

	public void setIdSobreaviso(int idSobreaviso) {
		this.idSobreaviso = idSobreaviso;
	}

	public int getFuncMatricula() {
		return funcMatricula;
	}

	public void setFuncMatricula(int funcMatricula) {
		this.funcMatricula = funcMatricula;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

}
