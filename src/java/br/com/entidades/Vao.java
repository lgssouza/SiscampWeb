package br.com.entidades;

public class Vao {

    private int idVao;
    private int idEstInicial;
    private int idEstFinal;
    private Float distancia;
    private Float progressiva;
    private Float regressiva;
    private int sequencia;

    public int getIdVao() {
        return idVao;
    }

    public void setIdVao(int idVao) {
        this.idVao = idVao;
    }

    public int getIdEstInicial() {
        return idEstInicial;
    }

    public void setIdEstInicial(int idEstInicial) {
        this.idEstInicial = idEstInicial;
    }

    public int getIdEstFinal() {
        return idEstFinal;
    }

    public void setIdEstFinal(int idEstFinal) {
        this.idEstFinal = idEstFinal;
    }

    public Float getDistancia() {
        return distancia;
    }

    public void setDistancia(Float distancia) {
        this.distancia = distancia;
    }

    public Float getProgressiva() {
        return progressiva;
    }

    public void setProgressiva(Float progressiva) {
        this.progressiva = progressiva;
    }

    public Float getRegressiva() {
        return regressiva;
    }

    public void setRegressiva(Float regressiva) {
        this.regressiva = regressiva;
    }

    public int getSequencia() {
        return sequencia;
    }

    public void setSequencia(int sequencia) {
        this.sequencia = sequencia;
    }

}
