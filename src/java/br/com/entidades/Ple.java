package br.com.entidades;

public class Ple {

	private int idPle;
	private String supervisor;
	private String data;
	private String hrInicial;
	private String hrFinal;
	private int idLinhaDist;

	public int getIdPle() {
		return idPle;
	}

	public void setIdPle(int idPle) {
		this.idPle = idPle;
	}

	public String getSupervisor() {
		return supervisor;
	}

	public void setSupervisor(String supervisor) {
		this.supervisor = supervisor;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getHrInicial() {
		return hrInicial;
	}

	public void setHrInicial(String hrInicial) {
		this.hrInicial = hrInicial;
	}

	public String getHrFinal() {
		return hrFinal;
	}

	public void setHrFinal(String hrFinal) {
		this.hrFinal = hrFinal;
	}

	public int getIdLinhaDist() {
		return idLinhaDist;
	}

	public void setIdLinhaDist(int idLinhaDist) {
		this.idLinhaDist = idLinhaDist;
	}

}
